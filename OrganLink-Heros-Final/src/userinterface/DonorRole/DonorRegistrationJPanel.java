    /**
 * 检查字符串是否包含数字
 */
    private boolean containsDigit(String str) {
        for (char c : str.toCharArray()) {
           if (Character.isDigit(c)) {
              return true;
        }
      }
        return false;
    }

/**
 * 检查字符串是否全是数字
 */
    private boolean isAllDigits(String str) {
        if (str == null || str.isEmpty()) {
          return false;
    }
    for (char c : str.toCharArray()) {
        if (!Character.isDigit(c) && c != ' ') {  // 允许空格
            return false;
        }
      }
    return true;
    }

