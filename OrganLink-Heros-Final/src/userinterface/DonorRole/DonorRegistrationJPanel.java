    /**
     * 解析出生日期字符串 (格式: YYYY-MM-DD)
     */
    private Date parseDateOfBirth(String dobStr) {
        if (dobStr == null || dobStr.trim().isEmpty()) {
            return null;
        }
        
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            sdf.setLenient(false); // 嚴格驗證日期
            return sdf.parse(dobStr.trim());
        } catch (ParseException e) {
            return null;
        }
    }
    
    /**
     * 驗證所有必填欄位
     */
    private boolean validateInput() {
        // 驗證姓名
        if (txtName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your name!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        // 验证姓名不能包含数字
        if (containsDigit(txtName.getText().trim())) {
           JOptionPane.showMessageDialog(this, 
        "Name cannot contain numbers!", 
        "Validation Error", 
        JOptionPane.WARNING_MESSAGE);
           return false;
        }
        
        // Validate DOB format and value
        if (txtDOB.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your date of birth!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    
        Date dobDate = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy");
            sdf.setLenient(false);
            dobDate = sdf.parse(txtDOB.getText().trim());
        
            // Check if date is in the future
            if (dobDate.after(new Date())) {
                JOptionPane.showMessageDialog(this, "Date of birth cannot be in the future!", 
                    "Validation Error", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Invalid date format! Please use MM/DD/YYYY", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    
        // Validate age (auto-calculated from DOB)
        if (txtAge.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a valid date of birth to calculate age!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    
        try {
            int age = Integer.parseInt(txtAge.getText().trim());
            if (age < 18 || age > 75) {
                JOptionPane.showMessageDialog(this, 
                    "Donors must be between 18 and 75 years old!", 
                    "Validation Error", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Invalid age value!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    
        // 驗證 Email
        if (txtEmails.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (!txtEmails.getText().trim().contains("@")) {
            JOptionPane.showMessageDialog(this, 
                "Please enter a valid email address!", 
                "Validation Error", 
                JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // Validate phone number - must be exactly 10 digits
        if (txtPhoneNumber.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your phone number!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    
        String phoneDigits = txtPhoneNumber.getText().trim().replaceAll("[^0-9]", "");
        if (phoneDigits.length() != 10) {
            JOptionPane.showMessageDialog(this, "Phone number must be exactly 10 digits!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // 驗證 PIN
        if (txtPIN.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your PIN!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // 驗證血型
        if (ComboBoxBloodType.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please select your blood type!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // 驗證器官類型
        if (txtOrganType.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please select organ type!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // 驗證地址
        if (txtStreetAddress.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your street address!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        // 验证地址不能全是数字
        if (isAllDigits(txtStreetAddress.getText().trim())) {
           JOptionPane.showMessageDialog(this, 
        "Street address cannot be only numbers!", 
        "Validation Error", 
        JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (txtCity.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your city!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // 验证城市名不能包含数字
        if (containsDigit(txtCity.getText().trim())) {
            JOptionPane.showMessageDialog(this, 
        "City name cannot contain numbers!", 
        "Validation Error", 
        JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (txtZipCode.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your zip code!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // Validate zip code - must be exactly 5 digits
        if (txtZipCode.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your zip code!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    
        String zipDigits = txtZipCode.getText().trim().replaceAll("[^0-9]", "");
        if (zipDigits.length() != 5) {
            JOptionPane.showMessageDialog(this, "Zip code must be exactly 5 digits!", 
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        // 驗證健康問題
        if (!btnYesQ1.isSelected() && !btnNoQ1.isSelected()) {
            JOptionPane.showMessageDialog(this, 
                "Please answer: Do you have any chronic conditions?", 
                "Health Screening Required", 
                JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (!btnYesQ2.isSelected() && !btnNoQ2.isSelected()) {
            JOptionPane.showMessageDialog(this, 
                "Please answer: Are you currently feeling unwell?", 
                "Health Screening Required", 
                JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (!btnYesQ4.isSelected() && !btnNoQ3.isSelected()) {
            JOptionPane.showMessageDialog(this, 
                "Please answer: Do you have any contagious diseases?", 
                "Health Screening Required", 
                JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }
    
    /**
     * 檢查健康資格 - 所有問題都必須回答 No
     */
