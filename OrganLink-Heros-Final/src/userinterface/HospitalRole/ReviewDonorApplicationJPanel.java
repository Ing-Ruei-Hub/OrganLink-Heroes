    private void populateTable() {
        
        // ============ 调试信息 ============
    System.out.println("=== HOSPITAL OFFICER DEBUG ===");
    System.out.println("Hospital Organization: " + organization.getName());
    System.out.println("Organization ID: " + organization.getOrganizationID());
    System.out.println("WorkQueue Size: " + organization.getWorkQueue().getWorkRequestList().size());
    System.out.println("All Requests in WorkQueue:");
    for (int i = 0; i < organization.getWorkQueue().getWorkRequestList().size(); i++) {
        System.out.println("  - Request " + i + ": " + organization.getWorkQueue().getWorkRequestList().get(i).getClass().getName());
        if (organization.getWorkQueue().getWorkRequestList().get(i) instanceof DonorRegistrationRequest) {
            DonorRegistrationRequest req = (DonorRegistrationRequest) organization.getWorkQueue().getWorkRequestList().get(i);
            System.out.println("    ID: " + req.getRequestID() + ", Status: " + req.getStatus());
        }
    }
    System.out.println("==============================");
    // ============ End ============
    
    DefaultTableModel model = (DefaultTableModel) tblReviewDonor.getModel();
    model.setRowCount(0);
    
    // ============ 修改：从 Enterprise 中找到 Donor Organization ============
    WorkQueue workQueue = null;
    
    // 遍历 Enterprise 中的所有 Organization，找到 Donor Organization
    for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
        if (org.getName().equals("Donor Organization")) {
            workQueue = org.getWorkQueue();
            System.out.println("Found Donor Organization! ID: " + org.getOrganizationID());
            System.out.println("WorkQueue Size: " + workQueue.getWorkRequestList().size());
            break;
        }
    }
    
    // 如果没找到 Donor Organization，显示错误
    if (workQueue == null) {
        JOptionPane.showMessageDialog(this, 
            "Error: Cannot find Donor Organization in this Enterprise!", 
            "Configuration Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    // ============ End 修改 ============
    
    // 现在使用找到的 workQueue
    for (int i = 0; i < workQueue.getWorkRequestList().size(); i++) {
        if (workQueue.getWorkRequestList().get(i) instanceof DonorRegistrationRequest) {
            DonorRegistrationRequest request = (DonorRegistrationRequest) workQueue.getWorkRequestList().get(i);
            
            if ("Pending Hospital Review".equals(request.getStatus())) {
                Object[] row = new Object[5];
                row[0] = request.getRequestID();
                row[1] = request.getDonorName();
                row[2] = request.getBloodType();
                row[3] = request.getOrganType();
                row[4] = request.getStatus();
                
                model.addRow(row);
            }
        }
      }
    }

