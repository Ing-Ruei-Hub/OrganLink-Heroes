    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
          int selectedRow = tblReviewDonor.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, 
                "Please select a donor application to approve.", 
                "Warning", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // 获取选中的 Request ID
        String requestID = tblReviewDonor.getValueAt(selectedRow, 0).toString();
        String donorName = tblReviewDonor.getValueAt(selectedRow, 1).toString();
        
        // 确认操作
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to approve the application for: " + donorName + "?", 
            "Confirm Approval", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        // 请求输入审核意见（可选）
        String comments = JOptionPane.showInputDialog(this, 
            "Enter approval comments (optional):", 
            "Approval Comments", 
            JOptionPane.QUESTION_MESSAGE);
        
        // 找到对应的 Request
        DonorRegistrationRequest selectedRequest = findRequestByID(requestID);
        
        if (selectedRequest != null) {
            // 更新 Request 状态
            selectedRequest.setStatus("Approved by Hospital");
            selectedRequest.setResolveDate(new Date());
            selectedRequest.setResolver(userAccount);
            selectedRequest.setReviewComments(comments);
            
            // 同时更新 Donor 对象的状态
            if (selectedRequest.getDonor() != null) {
                selectedRequest.getDonor().setStatus("Approved by Hospital");
            }
            
            JOptionPane.showMessageDialog(this, 
                "✅ Donor application for " + donorName + " has been approved successfully!", 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE);
            
            // 刷新表格
            populateTable();
        } else {
            JOptionPane.showMessageDialog(this, 
                "Request not found!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
                                              
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:
       int selectedRow = tblReviewDonor.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, 
                "Please select a donor application to reject.", 
                "Warning", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // 获取选中的 Request ID
        String requestID = tblReviewDonor.getValueAt(selectedRow, 0).toString();
        String donorName = tblReviewDonor.getValueAt(selectedRow, 1).toString();
        
        // 请求输入拒绝原因（必填）
        String reason = JOptionPane.showInputDialog(this, 
            "Enter rejection reason (required):", 
            "Rejection Reason", 
            JOptionPane.QUESTION_MESSAGE);
        
        if (reason == null || reason.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Rejection reason is required!", 
                "Warning", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // 确认操作
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to reject the application for: " + donorName + "?", 
            "Confirm Rejection", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        // 找到对应的 Request
        DonorRegistrationRequest selectedRequest = findRequestByID(requestID);
        
        if (selectedRequest != null) {
            // 更新 Request 状态
            selectedRequest.setStatus("Rejected by Hospital");
            selectedRequest.setResolveDate(new Date());
            selectedRequest.setResolver(userAccount);
            selectedRequest.setReviewComments(reason);
            
            // 同时更新 Donor 对象的状态
            if (selectedRequest.getDonor() != null) {
                selectedRequest.getDonor().setStatus("Rejected by Hospital");
            }
            
            JOptionPane.showMessageDialog(this, 
                "❌ Donor application for " + donorName + " has been rejected.", 
                "Rejected", 
                JOptionPane.INFORMATION_MESSAGE);
            
            // 刷新表格
            populateTable();
        } else {
            JOptionPane.showMessageDialog(this, 
                "Request not found!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRejectActionPerformed

